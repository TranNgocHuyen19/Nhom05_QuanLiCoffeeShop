private Category revertCategory() {
        String ten = txtDanhMuc.getText().trim();
        String moTa = txtMoTa.getText().trim();
        return new Category(ten, moTa);
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
        if (Validata()) {
            Category cate = revertCategory();
            if (dc.insert(cate)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                tblModelCate.addRow(new Object[]{cate.getCategoryID(), cate.getName(), cate.getDescription()});
                int countCus = dc.findAll().size();
                if (countCus > 0) {
                    lblCountDM.setText(countCus + "");
                } else {
                    lblCountDM.setText("0");
                }
//                dc.insert(cate);
                gui.CreateOrder.tabCat.add(cate.getName(), new JScrollPane());
            } else {
                JOptionPane.showMessageDialog(null, "Danh mục đã tồn tại này đã tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_btnThemActionPerformed
    boolean Validata() {
        String name = txtDanhMuc.getText().trim();
        if (name.equals("")) {
            errorTen.setForeground(Color.RED);
            errorTen.setText("Tên không hợp lệ");
            return false;
        } else {
            errorTen.setForeground(Color.GREEN);
            errorTen.setText(" V ");
        }
        String moTa = txtMoTa.getText().trim();
        if (moTa.equals("")) {
            errorMoTa.setForeground(Color.RED);
            errorMoTa.setText("Mô tả không được để trống !");
            return false;
        } else {
            errorMoTa.setForeground(Color.GREEN);
            errorMoTa.setText(" V ");
        }
        return true;

    }
    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (Validata()) {
            int row = table.getSelectedRow();
            String name = txtDanhMuc.getText();
            String desc = txtMoTa.getText();

            String cateID = tblModelCate.getValueAt(row, 0).toString();

            tblModelCate.setValueAt(txtDanhMuc.getText(), row, 1);
            tblModelCate.setValueAt(txtMoTa.getText(), row, 2);

            Category newCategory = new Category(cateID, name, desc);
            dc.update(newCategory);
            String newCateID = newCategory.getCategoryID();
            int indexTabCat = Integer.parseInt(newCateID.substring(1, newCateID.length()));

            gui.CreateOrder.tabCat.setTitleAt(indexTabCat - 1, newCategory.getName());
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

