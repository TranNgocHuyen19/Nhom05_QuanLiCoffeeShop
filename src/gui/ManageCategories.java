    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        tblModelCate.setRowCount(0);
        String txtFind = txtTim.getText().trim();
        Category cate = dc.findById(txtFind);

        if (cate != null) {
            tblModelCate.addRow(new Object[]{cate.getCategoryID(), cate.getName(), cate.getDescription()});
            int countCus = dc.findAll().size();
            if (countCus > 0) {
                lblCountDM.setText(countCus + "");
            } else {
                lblCountDM.setText("0");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy !");
            loadData();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

