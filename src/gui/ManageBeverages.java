    private void btnImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImgActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter imgFilter = new FileNameExtensionFilter("hinh anh", "jpg");
        fileChooser.setFileFilter(imgFilter);
        fileChooser.setMultiSelectionEnabled(false);

        int x = fileChooser.showDialog(this, "Choose file");
        if (x == JFileChooser.APPROVE_OPTION) {
            File f = fileChooser.getSelectedFile();
            
            String imgPath = f.getAbsolutePath();
            imgPathCut = imgPath.substring(imgPath.lastIndexOf("\\") + 1);
            File newF = new File("./src/img/" + f.getName());
            try {
                // Copy file
                Files.copy(f.toPath(), newF.toPath(), StandardCopyOption.REPLACE_EXISTING);

                BufferedImage originalImage = ImageIO.read(f);

                // Kích thước mới
                int newWidth = 100;
                int newHeight = 100;

                // Resize ảnh
                BufferedImage resizedImage = new BufferedImage(newWidth, newHeight, BufferedImage.TYPE_INT_ARGB);
                Graphics2D g = resizedImage.createGraphics();
                g.drawImage(originalImage, 0, 0, newWidth, newHeight, null);
                g.dispose();

                // Hiển thị ảnh đã resize trên JLabel
                lblImg.setIcon(new ImageIcon(resizedImage));
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnImgActionPerformed

