    private boolean valiData() {
        String catBev = cbbNameCatBev.getSelectedItem().toString();
        double taxBev = 0;
        String nameBev = txtNameBev.getText().trim();
        String price = txtPriceBev.getText().trim();
        double priceBev = 0;
        if (nameBev.equals("")) {
            errorNameBev.setForeground(Color.red);
            errorNameBev.setText("Tên không hợp lệ!");
            return false;
        } else {
            errorNameBev.setForeground(Color.green);
            errorNameBev.setText("");

        }

        if (!price.matches("[0-9]+")) {
            errorPriceBev.setForeground(Color.red);
            errorPriceBev.setText("Giá không hợp lệ");
            return false;
        } else {
            priceBev = Double.parseDouble(price);
            errorPriceBev.setForeground(Color.green);
            errorPriceBev.setText("");

        }
        return true;
    }

    private Beverage revertBeverageFromTextFields() {
        String catBev = cbbNameCatBev.getSelectedItem().toString();
        double taxBev = 0;
        String nameBev = txtNameBev.getText().trim();
        String price = txtPriceBev.getText().trim();
        double priceBev = Double.parseDouble(price);

        Beverage newBev = new Beverage(dc.findById(catBev), nameBev, priceBev);

        return new Beverage(newBev.getBeverageID(), nameBev,
                LocalDateTime.now(), false,
                priceBev, 0, imgPathCut, dc.findById(catBev));
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblBev.getSelectedRow();
//        String catBev = cbbNameCatBev.getSelectedItem().toString();
        String catBev = tblModelBev.getValueAt(row, 0).toString();
        String nameBev = txtNameBev.getText().trim();
        String bevID = tblModelBev.getValueAt(row, 1).toString();
        double priceBev = Double.parseDouble(txtPriceBev.getText().trim());
        boolean disBev = cbDiscontinued.isSelected();
        String image = tblModelBev.getValueAt(row, 5).toString();

        if (valiData()) {

            Beverage b = db.findById(bevID);

            Beverage newBev = new Beverage(b.getBeverageID(), nameBev,
                    LocalDateTime.now(), disBev,
                    priceBev, 0, image, db.findById(bevID).getCategory());

            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn cập nhật thông tin?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                tblModelBev.setValueAt(catBev, row, 0);
                tblModelBev.setValueAt(nameBev, row, 2);
                tblModelBev.setValueAt(priceBev, row, 3);
                tblModelBev.setValueAt(disBev, row, 4);
                tblModelBev.setValueAt(image, row, 5);
                db.update(newBev);
            }

        }


    }//GEN-LAST:event_btnUpdateActionPerformed

