    public void loadData() {
        List<Beverage> listTopBev = db.getListTopBev();
        for (Beverage bev : listTopBev) {
            tblModelBev.addRow(new Object[]{bev.getCategory().getName(),
                bev.getBeverageID(), bev.getName(),
                bev.getSellingPrice(), bev.isDiscontinued(), bev.getImgPath()});
            int countBev = db.findAll().size();
            if (countBev > 0) {
                txtTotal.setText(countBev + "");
            } else {
                txtTotal.setText("0");
            }
        }

        int count = dc.findAll().size();
        for (int i = 0; i < count; i++) {
            cbbNameCatBev.addItem(dc.findAll().get(i).getCategoryID());
        }

    }

