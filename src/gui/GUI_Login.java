    private void setImage(String imgPath) {
        try {
            BufferedImage originalImg = ImageIO.read(getClass().getResource(imgPath));
            int newWidth = 1250;
            int newHeight = 1200; 

            Image resizedImg = originalImg.getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH);
            ImageIcon imgIcon = new ImageIcon(resizedImg);
            lblImg.setIcon(imgIcon); // Đặt hình ảnh đã thay đổi kích thước vào nhãn
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

