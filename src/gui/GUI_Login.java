    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        btnLogin.setEnabled(false);
        String username = txtUsername.getText().trim();
        String password = String.valueOf(txtPass.getPassword());
        if (username.length() == 0) {
            JOptionPane.showMessageDialog(this, "Cần nhập tên đăng nhập");
            txtUsername.requestFocus();
             btnLogin.setEnabled(true);
            return;
        }

        if (password.length() == 0) {
            JOptionPane.showMessageDialog(this, "Cần nhập mật khẩu");
            txtPass.requestFocus();
            return;
        }
        e = de.findPasswordByUsername(username, password);
        if (e != null) {
            btnLogin.setEnabled(false);
//            JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
            new GUI_Main().setVisible(true);
            this.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu sai");
            btnLogin.setEnabled(true);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

