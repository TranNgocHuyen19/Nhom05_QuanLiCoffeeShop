    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        tblModelCus.setRowCount(0);
        String find = txtSearch.getText();
        Customer c = db.findByPhone(find);
        if (c != null) {
            tblModelCus.addRow(new Object[]{c.getCustomerID(), c.getName(), c.getPhoneNumber(), c.getRewardPoint()});

            int countCus = db.findAll().size();
            if (countCus > 0) {
                txtTotal.setText(countCus + "");
            } else {
                txtTotal.setText("0");
            }

            txtNameCus.setText(tblModelCus.getValueAt(row, 1).toString());
            txtSDT.setText(tblModelCus.getValueAt(row, 2).toString());
        } else {
            clearTextField();
            loadData();
            JOptionPane.showMessageDialog(this, "Không tìm thấy!");

        }
    }//GEN-LAST:event_btnSearchActionPerformed

