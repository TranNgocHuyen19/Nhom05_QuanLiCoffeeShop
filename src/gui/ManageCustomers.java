    public void loadData() {
        List<Customer> listTopCus = db.getListTopCus();
        for (Customer cus : listTopCus) {
            if(cus.getCustomerID().equals("Cust0000"))
                return;
            tblModelCus.addRow(new Object[]{cus.getCustomerID(), cus.getName(), cus.getPhoneNumber(), cus.getRewardPoint()});
            int countCus = db.findAll().size();
            if (countCus > 0) {
                txtTotal.setText(countCus + "");
            } else {
                txtTotal.setText("0");
            }
        }
    }

